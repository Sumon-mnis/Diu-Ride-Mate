export const getLocationsByDivision = (division: string) => {
  return bangladeshLocations.filter(location => location.division === division);
};

export const getLocationsByArea = (area: string) => {
  return bangladeshLocations.filter(location => location.area === area);
};

export const searchLocations = (query: string) => {
  const searchTerm = query.toLowerCase();
  return bangladeshLocations.filter(location => 
    location.name.toLowerCase().includes(searchTerm) ||
    location.area.toLowerCase().includes(searchTerm) ||
    location.division.toLowerCase().includes(searchTerm)
  );
};

export const getPopularDhakaLocations = () => {
  return bangladeshLocations.filter(location => 
    location.division === "Dhaka" && 
    ["Dhanmondi", "Gulshan", "Banani", "Uttara", "Mirpur", "Motijheel", "Farmgate", "Panthapath", "Bashundhara", "DIU Campus"].includes(location.name)
  );
};